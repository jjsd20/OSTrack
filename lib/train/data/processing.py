    def _normalize_gt_sequence(self, gt_sequence_anno, image_size):
        """
        Normalize GT sequence annotations relative to image size.
        
        args:
            gt_sequence_anno - GT sequence annotations tensor of shape (1, seq_len, 4)
            image_size - Image size [H, W]
            
        returns:
            torch.Tensor - Normalized GT sequence annotations in [0,1] range
        """
        if gt_sequence_anno is None or gt_sequence_anno.numel() == 0:
            return gt_sequence_anno
            
        # Remove the batch dimension for processing
        gt_anno = gt_sequence_anno.squeeze(0)  # (seq_len, 4)
        
        # Get image dimensions
        H, W = image_size[0], image_size[1]
        
        # Normalize coordinates to [0,1] range
        normalized_gt = gt_anno.clone()
        normalized_gt[:, 0] = normalized_gt[:, 0] / W  # x
        normalized_gt[:, 1] = normalized_gt[:, 1] / H  # y  
        normalized_gt[:, 2] = normalized_gt[:, 2] / W  # w
        normalized_gt[:, 3] = normalized_gt[:, 3] / H  # h
        
        # Clamp to [0,1] range
        normalized_gt = torch.clamp(normalized_gt, 0.0, 1.0)
        
        # Stack back to tensor format
        return normalized_gt.unsqueeze(0)  # (1, seq_len, 4)


    def _process_gt_sequence_annotations(self, gt_sequence_anno, jittered_anno,
                                         search_area_factor, output_sz, mode='search'):
        """
        Process GT sequence annotations to maintain consistency with jittered template and search boxes.

        args:
            gt_sequence_anno - GT sequence annotations tensor of shape (1, seq_len, 4)
            jittered_anno - Jittered annotations for reference
            search_area_factor - Search area factor for coordinate transformation
            output_sz - Output size for coordinate transformation
            mode - 'template' or 'search' mode

        returns:
            torch.Tensor - Processed GT sequence annotations in normalized coordinates
        """
        if gt_sequence_anno is None or gt_sequence_anno.numel() == 0:
            return gt_sequence_anno

        # Remove the batch dimension for processing
        gt_anno = gt_sequence_anno.squeeze(0)  # (seq_len, 4)

        # Get the crop parameters from jittered annotations
        ref_box = jittered_anno[0] if isinstance(jittered_anno, list) else jittered_anno
        ref_center = ref_box[0:2] + 0.5 * ref_box[2:4]
        ref_size = ref_box[2:4]

        # Calculate crop parameters
        crop_sz = torch.sqrt(ref_size.prod()) * search_area_factor
        crop_sz = torch.ceil(crop_sz)

        # Transform GT sequence coordinates to crop-relative coordinates
        processed_gt_anno = []
        for i in range(gt_anno.shape[0]):
            bbox = gt_anno[i]  # (4,)

            # Convert to center format
            bbox_center = bbox[0:2] + 0.5 * bbox[2:4]
            bbox_size = bbox[2:4]

            # Calculate relative coordinates in the crop
            rel_center = (bbox_center - ref_center) / crop_sz + 0.5
            rel_size = bbox_size / crop_sz

            # Convert back to corner format
            rel_bbox = torch.cat((rel_center - 0.5 * rel_size, rel_size), dim=0)

            # Scale to output size and normalize to [0,1]
            output_bbox = rel_bbox * output_sz
            normalized_bbox = output_bbox / output_sz

            processed_gt_anno.append(normalized_bbox)

        # Stack back to tensor format
        processed_gt_anno = torch.stack(processed_gt_anno, dim=0)  # (seq_len, 4)
        return processed_gt_anno.unsqueeze(0)  # (1, seq_len, 4)